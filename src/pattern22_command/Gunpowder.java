package pattern22_command;

/**
 * 火药
 */
public class Gunpowder {
    // 燃烧绿色火焰
    public void fireGreen() {
        System.out.println("燃烧绿色火焰");
    }
    // 燃烧红色火焰
    public void fireRed() {
        System.out.println("燃烧红色火焰");
    }
    // 燃烧爆炸响声的火药
    public void fireSound() {
        System.out.println("咚~~~~~");
    }
}
